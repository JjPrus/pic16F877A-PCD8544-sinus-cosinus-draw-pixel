void command(char c){
    RC0 = 0;
    spiWrite(c);
    __delay_ms(1);
}

void data(char c){
    RC0 = 1;
    spiWrite(c);
    __delay_ms(1);
}

void lcdHome(){
    command(PCD8544_SETYADDR | 0);
    command(PCD8544_SETXADDR | 0);
    RC0 = 1;   
}

void lcdCursor(int x, int y){
    command(PCD8544_SETYADDR | y);
    command(PCD8544_SETXADDR | x);
    RC0 = 1;   
}

void lcdBegin(){
    TRISC = 0;
    
    __delay_ms(500);
    spiBegin(MASTER_OSC_DIV4, SAMPLE_MIDDLE, IDLE_TO_ACTIVE, IDLE_LOW);
    __delay_ms(500);
    
    int contrast = 40;
    int bias = 0x04;
    //reset LCD
    RC1 = 0;
    __delay_ms(500);
    RC1 = 1;
    //--------------------------------------------------------------------------
    
    command(PCD8544_FUNCTIONSET | PCD8544_EXTENDEDINSTRUCTION);
    command(PCD8544_SETBIAS | bias);
    command(PCD8544_SETVOP | contrast);
    command(PCD8544_FUNCTIONSET);
    command(PCD8544_DISPLAYCONTROL | PCD8544_DISPLAYNORMAL);
    lcdHome();
}

void lcdClear(){
     for(int index=0;index < (LCDWIDTH * LCDHEIGHT/8);index++){
        data(0x00);
    }
}

void lcdChar(char character) {
    data(0x00); 
    for (int index = 0 ; index < 5 ; index++){
            data(ASCII[character-0x20][index]);
    } 
}

void lcdPrint(char *characters) {
    while (*characters)
    lcdChar(*characters++);
}

